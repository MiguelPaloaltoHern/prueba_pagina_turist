    1. TÉRMINOS GENERALES
    
    
    La entidad responsable de este sitio, Secretaría Distrital de Ambiente en adelante “LA ENTIDAD”, es una entidad pública
    del orden distrital, esto quiere decir que depende totalmente de la Alcaldía Mayor de Bogotá. Su naturaleza jurídica la
    constituye como la entidad rectora del medio ambiente distrital y principal coordinadora de su ejecución.
    
    
    La función principal de este Sitio Web consiste en proveer información y servicios así como la de divulgar y promover
    normas, políticas y directrices relacionadas con el Objeto y las Funciones de LA ENTIDAD.
    
    
    
    Por medio del Sitio Web, LA ENTIDAD publica, entre otros, los temas y actividades que tienen que ver con su misión, su
    visión, objetivos y las funciones que le corresponden. Adicionalmente, por este medio LA ENTIDAD da a conocer
    información sobre Políticas, planes, programas y proyectos institucionales, Trámites; Servicios; Indicadores de Gestión;
    Planes y Programas; Publicaciones; Normas; Convocatorias; Información presupuestal y de contratación; páginas
    recomendadas, y, en general, información relacionada con el gobierno y la entidad o del los programas que desarrollan
    las entidades si es el caso. Adicionalmente, permite la opción de solicitar trámites en línea y ofrece herramientas de
    interacción para los usuarios del sitio.
    
    
    
    1.1 Glosario
    
    
    Para facilitar la comprensión de estas Condiciones de Uso del Sitio Web, se hace necesario aclarar el significado que se
    le dará a las siguientes palabras:
    
    
    a. Contenidos. Todos los elementos de información o datos que se divulgan en la página web, entre los que se encuentran:
    textos, imágenes, fotos, logos, diseños, animaciones.
    
    
    b. Derechos de Propiedad Intelectual. incluye lo relativo a marcas, nombres comerciales, logos, enseñas, lemas, nombres
    de dominio, secretos empresariales, saber-hacer, diseños industriales, patentes, modelos de utilidad y derecho de autor
    sobre cualquier tipo de obra perceptible por los sentidos.
    
    
    c. Foro. Servicio automatizado de mensajes, a menudo moderado por un administrador, a través del cual los suscriptores
    reciben mensajes dejados por otros suscriptores por un tema dado. Para acceder al foro se requiere hacerlo a través de
    un correo electrónico.
    
    
    d. Internet. Conjunto descentralizado de redes de comunicación interconectadas por la familia de protocolos TCP/IP.
    Sobre esta red se pueden utilizar múltiples servicios como por ejemplo correos electrónicos, Voz, vídeo, www, etc.
    
    
    e. Página web. Es un conjunto de hipertextos o hipermedias que proporcionan un navegador del www después de obtener la
    información solicitada. Su contenido puede incluir textos, imágenes, videos, sonido, etc.
    
    
    f. Publicar. Hacer que un documento sea visible desde el Sitio Web.
    
    
    g. Servicios. Son las ayudas en línea que LA ENTIDAD provee actualmente o que piensa proveer en el futuro a los
    usuarios, por medio de esta página web, como publicación de noticias o actividades propias de la gestión institucional;
    trámites en línea; consultas; foros y buzón de quejas y reclamos, entre otros.
    
    
    h. Usuario. Es toda persona que accede al Sitio Web.
    
    
    i. Enlace. Apuntadores hipertexto que sirven para saltar de una información a otra, o de un servidor web a otro, cuando
    se navega por Internet.
    
    1.2 Condiciones de Privacidad
    
    
    Se entiende por información personal aquella suministrada por el Usuario para el registro, la cual incluye datos como
    nombre, identificación, edad, género, dirección, correo electrónico y teléfono.
    
    
    El almacenamiento, y uso de la información personal se rige por las Políticas de Privacidad del Sitio Web.
    
    
    
    Es interés de LA ENTIDAD la salvaguarda de la privacidad de la información personal del USUARIO obtenida a través del
    Sitio Web, para lo cual se compromete a adoptar una política de confidencialidad de acuerdo con lo que se establece a
    continuación:
    
    
    
    El USUARIO reconoce que el ingreso de información personal, lo realiza de manera voluntaria y ante la solicitud de
    requerimientos específicos por LA ENTIDAD para realizar un trámite, presentar una queja o reclamo, o para acceder a los
    mecanismos interactivos.
    
    
    
    EL USUARIO acepta que a través del registro en el Sitio Web, LA ENTIDAD recoge datos personales, los cuales no se
    cederán a terceros sin su conocimiento.
    
    
    La recolección y tratamiento automatizado de los datos personales, como consecuencia de la navegación y/o registro por
    el Sitio Web tiene como finalidades las detalladas a continuación: (i) la adecuada gestión y administración de los
    servicios ofrecidos en el Sitio Web, en los que el Usuario decida darse de alta, utilizar o contratar; (ii) el estudio
    cuantitativo y cualitativo de las visitas y de la utilización de los servicios por parte de los usuarios; (iii) el envío
    por medios tradicionales y electrónicos de información relacionados con LA ENTIDAD y de cualquier otro proyecto de las
    entidades del orden Distrital, sus programas y sus entidades adscritas y vinculadas; (iv) poder tramitar servicios de
    gobierno en línea.
